<h6 style="text-align: justify; margin-right: 10%; margin-left: 10%; line-height: 1.6"> Saat ini Indonesia memiliki tuntutan untuk memiliki pemerintahan yang baik. Hal tersebut
bukanlah hal yang tidak mungkin untuk dicapai. Dengan perkembangan teknologi yang ada,
good governance dapat dicapai dengan menerapkan e-Government. Pada tahun 2003, dibangun
inisiatif untuk mengembangkan e-Government melalui Inpres No, 3 tahun 2003. Namun cukup
disayangkan, e-Government yang dikembangkan mengindikasikan hanya sekedar pemenuhan
terhadap kebijakan tersebut tanpa disertai kualitas. Berbagai hasil survey menunjukkan bahwa
Indonesia masih jauh tertinggal dalam hal adopsi e-Government apalagi jika dibandingkan
dengan negara maju. Hal ini ditambah lagi fakta bahwa sebagian besar proyek e-Government di
negara berkembang termasuk Indonesia mengalami kegagalan yang tinggi mencapai hingga 85%
sedangkan tingkat keberhasilan hanya mencapai 15%. Pada penelitian sebelumnya, telah
dihasilkan model kematangan implementasi e-Government yang dapat digunakan untuk
mengukur sejauh mana penerapan e-Government namun model tersebut belum dilakukan ujicoba
dan validasi. Oleh karena itu pada penelitian lanjutan ini akan dilakukan ujicoba model
kematangan e-Government di beberapa lokus penelitian terpilih dengan tujuan agar model
kematangan tersebut dapat menjamin keberhasilan organisasi. Metode yang digunakan dalam
ujicoba model kematangan e-Government adalah metode generik De Bruin yang terdiri dari 6
fase yaitu fase scope, design, populate, test, deploy dan maintain. Hasil ujicoba juga akan
dibandingkan dengan model kematangan yang sudah untuk melihat gap pengukuran dengan
model sebelumnya. Untuk melakukan validasi model kematangan e-Government dalam rangka
mendukung keberterimaan model secara luas maka pendekatan kualitatif berupa FGD (Focus
Group Discussion) digunakan dengan melibatkan pakar (expert) yang terkait. Penyempurnaan
model kematangan pada tahap akhir dilakukan untuk memastikan bahwa model tersebut dapat
diterapkan pada institusi pemerintah khususnya tingkat kabupaten/kota.</h6>